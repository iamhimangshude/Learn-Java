public class DataTypes {
    public static void main(String[] a) {
        // integer
        int num1 = 9;
        byte by = 127;
        short sh = 558;
        long l = 5854l;

        // decimal
        float f = 5.6f;
        double d = 5.6;

        // characters
        char c = 'k';

        // booleans
        boolean b = true;
    }
}
